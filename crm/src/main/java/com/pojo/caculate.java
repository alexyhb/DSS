package com.pojo;

public class caculate {
    private int id;
    private int customerId;
    private int companyId;
    private int sacleScore;
    private int nationScore;
    private int quantityScore;
    private int totalScore;

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public int getCustomerId() {
        return customerId;
    }

    public void setCustomerId(int customerId) {
        this.customerId = customerId;
    }

    public int getCompanyId() {
        return companyId;
    }

    public void setCompanyId(int companyId) {
        this.companyId = companyId;
    }

    public int getSacleScore() {
        return sacleScore;
    }

    public void setSacleScore(int sacleScore) {
        this.sacleScore = sacleScore;
    }

    public int getNationScore() {
        return nationScore;
    }

    public void setNationScore(int nationScore) {
        this.nationScore = nationScore;
    }

    public int getQuantityScore() {
        return quantityScore;
    }

    public void setQuantityScore(int quantityScore) {
        this.quantityScore = quantityScore;
    }

    public int getTotalScore() {
        return totalScore;
    }

    public void setTotalScore(int totalScore) {
        this.totalScore = totalScore;
    }
}
